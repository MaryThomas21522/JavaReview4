package review3;

public class ForLoop {
    public static void main(String[] args) {

        /*for loop used when we know in advance how many times
        we want to repeat block of code
        Example: you asked the user to enter 10 numbers

         */

        for(int i=1; i<5; i++){
            System.out.println("Hello");
            //Output: Hello Hello Hello Hello
            //It repeated four time because i++
            //there is only four Hello because i<5
        }

    }
}
