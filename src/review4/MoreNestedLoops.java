package review4;

public class MoreNestedLoops {

    public static void main(String[] args) {
    /*
    Asel gave an example of the old school car speedometer
     */

        for (int a = 0; a <=9; a++) {

            for (int b=0; b<=9; b++){

                for (int c = 0; c <=9; c++) {

                    System.out.println(a+" "+b+" "+c);

                }

            }

        }




    }
}
